"""#### Mini-desafio Diseniar un programa en el cual el usuario ingrese tres numeros,
 uno a la vez, y se muestre a la salida el promedio de los tres numeros.####"""


x=int(input("Ingrese el valor de la variable x: "))
y=int(input("Ingrese el valor de la variable y: "))
z=int(input("Ingrese el valor de la variable z: "))


promedio=(x+y+z)/3
print(promedio)